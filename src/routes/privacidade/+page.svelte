<header>
	<h1>Política de Privacidade e Segurança</h1>
</header>
<section>
	<h2>1. Introdução</h2>
	<p>
		A Subimersa valoriza a privacidade de nossos usuários e está comprometida em protegê-la. Esta
		política descreve as práticas de coleta, uso e proteção de informações pessoais que você fornece
		ao usar nosso site. Ao acessar ou usar nosso site, você concorda com os termos desta política.
	</p>
</section>
<section>
	<h2>2. Coleta de Informações</h2>
	<p>
		Coletamos informações que você nos fornece ao se registrar em um curso, preencher um formulário
		ou se comunicar conosco. As informações podem incluir, mas não se limitam a, seu nome, endereço
		de e-mail, número de telefone e detalhes de pagamento.
	</p>
</section>
<section>
	<h2>3. Uso de Informações</h2>
	<p>
		Usamos as informações coletadas para: Processar inscrições e pagamentos de cursos; Comunicar
		informações importantes relacionadas aos cursos; Melhorar nossos serviços e conteúdo do site;
		Cumprir obrigações legais ou regulamentares.
	</p>
</section>
<section>
	<h2>4. Compartilhamento de Informações</h2>
	<p>
		Não vendemos, alugamos ou compartilhamos suas informações pessoais com terceiros, exceto
		conforme necessário para fornecer os serviços solicitados ou quando requerido por lei.
	</p>
</section>
<section>
	<h2>5. Segurança das Informações</h2>
	<p>
		Implementamos medidas de segurança técnicas e organizacionais adequadas para proteger suas
		informações pessoais contra acesso, alteração, divulgação ou destruição não autorizados.
		Mantemos suas informações pessoais pelo tempo que for necessário para cumprir os propósitos
		descritos nesta política, a menos que um período de retenção mais longo seja exigido ou
		permitido por lei.
	</p>
</section>
<section>
	<h2>6. Acesso e Controle de suas Informações</h2>
	<p>
		Você tem o direito de acessar, corrigir ou excluir suas informações pessoais que mantemos. Para
		exercer esses direitos, entre em contato conosco através das informações fornecidas na seção de
		contato do nosso site.
	</p>
</section>
<section>
	<h2>7. Alterações à Política de Privacidade</h2>
	<p>
		Podemos atualizar esta política de tempos em tempos. A versão mais recente será sempre publicada
		em nosso site. Encorajamos você a revisar periodicamente esta página para quaisquer mudanças.
	</p>
</section>
<section>
	<h2>8. Contato</h2>
	<p>
		Se você tiver dúvidas ou preocupações sobre nossa política de privacidade ou práticas de
		segurança, por favor entre em contato conosco através da nossa página de contato.
	</p>
</section>
